#!/usr/bin/env bash
# skill-metrics.sh — emit or update the skill-metrics table in README.md
#
# Per agentskills.io spec:
#   - description: max 1024 chars, ~100 tokens loaded at startup
#   - body (instructions): recommended < 5000 tokens, < 500 lines
#
# Token estimate: 1 token ≈ 4 chars (English prose average).
#
# Modes:
#   bin/skill-metrics.sh             # print the markdown table to stdout
#   bin/skill-metrics.sh --update     # rewrite the table block in README.md
#
# The table sits between these markers in README.md:
#   <!-- BEGIN skill-metrics -->
#   <!-- END skill-metrics -->
# When --update changes README, it also re-stages README so a pre-commit
# hook can ship the regenerated table in the same commit.

set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

MODE="print"
[[ $# -gt 0 && "$1" == "--update" ]] && MODE="update"

# ponytail: per-skill metrics via a single awk pass over each SKILL.md.
# Frontmatter is small; body char count uses length($0)+1 to include newline.
metrics() {
  find skills -type f -name SKILL.md | sort | while IFS= read -r skill; do
    awk -v skill="$skill" '
      BEGIN { fm = 0; name = ""; desc = ""; body_lines = 0; body_chars = 0 }
      /^---[[:space:]]*$/ {
        fm++
        if (fm == 2) { in_body = 1; next }
        next
      }
      fm == 1 {
        if ($0 ~ /^name:[[:space:]]*/) {
          n = $0
          sub(/^name:[[:space:]]*/, "", n)
          gsub(/^"|"$/, "", n)
          name = n
        }
        if ($0 ~ /^description:[[:space:]]*/) {
          d = $0
          sub(/^description:[[:space:]]*/, "", d)
          if (d ~ /^".*"$/) { sub(/^"/, "", d); sub(/"$/, "", d) }
          gsub(/\\"/, "\"", d)
          desc = d
        }
      }
      fm > 1 {
        body_lines++
        body_chars += length($0) + 1
      }
      END {
        if (name == "") {
          # fallback to parent directory name
          n = skill; sub(/.*\//, "", n); sub(/\/SKILL.md$/, "", n)
          slash = index(skill, "/skills/")
          pod = substr(skill, slash + length("/skills/"))
          sub(/\/SKILL.md$/, "", pod)
          name = pod
        }
        desc_chars = length(desc)
        desc_tokens = int((desc_chars + 3) / 4)
        body_tokens = int((body_chars + 3) / 4)
        # mark over-budget cells
        dc = desc_chars; dt = desc_tokens; bl = body_lines; bt = body_tokens
        dc_marker = ""; if (desc_chars > 1024) dc_marker = " ⚠"
        bl_marker = ""; if (body_lines >= 500) bl_marker = " ⚠"
        bt_marker = ""; if (body_tokens >= 5000) bt_marker = " ⚠"
        printf "| %s | %d%s | %d%s | %d%s | %d%s |\n", name, dc, dc_marker, dt, "", bl, bl_marker, bt, bt_marker
      }
    ' "$skill"
  done
}

# Aggregate totals for a footer summary
totals() {
  find skills -type f -name SKILL.md | sort | while IFS= read -r skill; do
    awk '
      BEGIN { fm = 0; desc = ""; body_chars = 0; body_lines = 0 }
      /^---[[:space:]]*$/ {
        fm++
        if (fm == 2) next
        next
      }
      fm == 1 {
        if ($0 ~ /^description:[[:space:]]*/) {
          d = $0
          sub(/^description:[[:space:]]*/, "", d)
          if (d ~ /^".*"$/) { sub(/^"/, "", d); sub(/"$/, "", d) }
          gsub(/\\"/, "\"", d)
          desc = d
        }
      }
      fm > 1 {
        body_lines++
        body_chars += length($0) + 1
      }
      END {
        print length(desc) "\t" int((length(desc) + 3) / 4) "\t" body_lines "\t" int((body_chars + 3) / 4)
      }
    ' "$skill"
  done | awk -F'\t' '
    { n++; dc += $1; dt += $2; bl += $3; bt += $4 }
    END {
      printf "TOTALS\t%d\t%d\t%d\t%d\t%d\n", n, dc, dt, bl, bt
    }
  '
}

build_table() {
  cat <<'EOF'
<!-- BEGIN skill-metrics -->
<!-- Auto-generated by bin/skill-metrics.sh. Do not edit by hand; run `bin/skill-metrics.sh --update` to refresh. -->

| Skill | Desc chars (max 1024) | Desc tokens (~100) | Body lines (max 500) | Body tokens (< 5000) |
|-------|---:|---:|---:|---:|
EOF
  metrics
  # Footer: totals row + spec-budget summary
  totals | awk -F'\t' '
    $1 == "TOTALS" {
      printf "| **Total (%d skills)** | **%d** | **%d** | **%d** | **%d** |\n", $2, $3, $4, $5, $6
      printf "\n_Budgets from [agentskills.io spec](https://agentskills.io/specification): description ≤ 1024 chars (~100 tokens metadata at startup), body < 5000 tokens & < 500 lines. ⚠ marks over-budget._\n"
    }
  '
  cat <<'EOF'
<!-- END skill-metrics -->
EOF
}

if [[ "$MODE" == "print" ]]; then
  build_table
  exit 0
fi

# --update mode: rewrite the marker block in README.md
README="README.md"
[[ -f "$README" ]] || { echo "README.md not found at $ROOT" >&2; exit 1; }

tmp="$(mktemp)"
trap 'rm -f "$tmp"' EXIT

in_block=0
saw_block=0
while IFS= read -r line; do
  if [[ "$line" == "<!-- BEGIN skill-metrics -->" ]]; then
    in_block=1
    saw_block=1
    build_table >>"$tmp"
    continue
  fi
  if [[ "$line" == "<!-- END skill-metrics -->" ]]; then
    in_block=0
    continue
  fi
  [[ "$in_block" == "1" ]] && continue
  printf '%s\n' "$line" >>"$tmp"
done <"$README"

if [[ "$saw_block" == "0" ]]; then
  echo "skill-metrics markers not found in $README" >&2
  echo "Add these lines to README.md where the table should appear:" >&2
  echo "  <!-- BEGIN skill-metrics -->" >&2
  echo "  <!-- END skill-metrics -->" >&2
  exit 1
fi

# Preserve trailing-newline state of README
if [[ -s "$README" && "$(tail -c1 "$README" 2>/dev/null)" == "" ]]; then
  printf '\n' >>"$tmp"  # was newline-terminated; keep it
fi

# Replace README atomically
mv "$tmp" "$README"

# Re-stage so a pre-commit hook ships the regenerated table in the same commit
git add "$README"
printf 'OK: skill-metrics table updated in %s (staged)\n' "$README"