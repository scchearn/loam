#!/usr/bin/env bash
# skill-metrics.sh — emit or update the skill-metrics table in README.md
#
# Per agentskills.io spec:
#   - description: max 1024 chars, ~100 tokens loaded at startup
#   - body (instructions): recommended < 5000 tokens, < 500 lines
#
# Token counts are real tiktoken cl100k_base counts via uv run --with tiktoken
# (no venv, no install — uv fetches tiktoken on first run and caches after).
#
# Modes:
#   bin/skill-metrics.sh             # print the markdown table to stdout
#   bin/skill-metrics.sh --update     # rewrite the table block in README.md and re-stage
#
# The table sits between these markers in README.md:
#   <!-- BEGIN skill-metrics -->
#   <!-- END skill-metrics -->

set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

MODE="print"
[[ $# -gt 0 && "$1" == "--update" ]] && MODE="update"

SKILLS="$(find skills -type f -name SKILL.md | sort)"

# Run the Python metrics helper under uv with tiktoken available.
# uv caches the environment after first run (~3s cold, <100ms warm).
METRICS_TSV="$(uv run --quiet --with tiktoken python3 bin/skill-metrics.py $SKILLS)"

# Format rows into markdown with thousands separators. No totals row, no over-budget markers —
# the table is for humans to see where each skill sits, not an enforcement gate.
format_rows() {
  printf '%s' "$METRICS_TSV" | awk -F'\t' '
    function comma(n) {
      s = ""
      n = n + 0
      while (n >= 1000) {
        r = n % 1000
        s = sprintf(",%03d%s", r, s)
        n = int(n / 1000)
      }
      s = n s
      sub(/^,/, "", s)
      return s
    }
    {
      printf "| %s | %s | %s | %s | %s |\n", $1, comma($2), comma($3), comma($4), comma($5)
    }
  '
}

build_table() {
  cat <<'EOF'
<!-- BEGIN skill-metrics -->
<!-- Auto-generated by bin/skill-metrics.sh via tiktoken cl100k_base. Do not edit by hand; run `bin/skill-metrics.sh --update` to refresh. -->

| Skill | Desc chars (max 1,024) | Desc tokens (~100) | Body lines (max 500) | Body tokens (< 5,000) |
|-------|---:|---:|---:|---:|
EOF
  format_rows
  cat <<'EOF'
<!-- END skill-metrics -->
EOF
}

if [[ "$MODE" == "print" ]]; then
  build_table
  exit 0
fi

# --update mode: rewrite the marker block in README.md
README="README.md"
[[ -f "$README" ]] || { echo "README.md not found at $ROOT" >&2; exit 1; }

tmp="$(mktemp)"
trap 'rm -f "$tmp"' EXIT

in_block=0
saw_block=0
while IFS= read -r line; do
  if [[ "$line" == "<!-- BEGIN skill-metrics -->" ]]; then
    in_block=1
    saw_block=1
    build_table >>"$tmp"
    continue
  fi
  if [[ "$line" == "<!-- END skill-metrics -->" ]]; then
    in_block=0
    continue
  fi
  [[ "$in_block" == "1" ]] && continue
  printf '%s\n' "$line" >>"$tmp"
done <"$README"

if [[ "$saw_block" == "0" ]]; then
  echo "skill-metrics markers not found in $README" >&2
  exit 1
fi

# Preserve trailing-newline state of README
if [[ -s "$README" && "$(tail -c1 "$README" 2>/dev/null)" == "" ]]; then
  printf '\n' >>"$tmp"
fi

mv "$tmp" "$README"
git add "$README"
printf 'OK: skill-metrics table updated in %s (staged)\n' "$README"